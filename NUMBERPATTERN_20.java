class NUMBERPATTERN_20
{
    public static void main()
    {
        int a=7,b=2;;
        for(int i=1;i<=7;i++)
        {
            for(int j=1;j<=a;j++)
            {
                System.out.print(j);
            }
            a--;
            System.out.println();
        }
        for(int i=1;i<=6;i++)
        {
            for(int j=1;j<=b;j++)
            {
                System.out.print(j);
            }
            b++;
            System.out.println();
        }
    }
}