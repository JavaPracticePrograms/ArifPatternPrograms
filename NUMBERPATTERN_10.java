class NUMBERPATTERN_10
{
    public static void main()
    {
        int a=1,b=4;
        for(int i=1;i<=5;i++)
        {
            for(int j=1;j<=a;j++)
            {
                System.out.print(i);
            }
            a++;
            System.out.println();
        }
        for(int i=4;i>=1;i--)
        {
            for(int l=1;l<=b;l++)
            {
                System.out.print(i);
            }
            b--;
            System.out.println();
        }
    }
}
 